package es.altia.flexia.notificacion.vo;


public class DireccionRegistroVO {

    private String direccion;
    private String descripcionMunicipio;
    private String codigoProvincia;
    private String codigoPais;
    private String codigoPostal;
    private String telfRemitente;
    private String faxRemitente;
    private String emailRemitente;
    private String nombreVia;

    /**
     * @return the direccion
     */
    public String getDireccion() {
        return direccion;
    }

    /**
     * @param direccion the direccion to set
     */
    public void setDireccion(String direccion) {
        this.direccion = direccion;
    }

    /**
     * @return the descripcionMunicipio
     */
    public String getDescripcionMunicipio() {
        return descripcionMunicipio;
    }

    /**
     * @param descripcionMunicipio the descripcionMunicipio to set
     */
    public void setDescripcionMunicipio(String descripcionMunicipio) {
        this.descripcionMunicipio = descripcionMunicipio;
    }

    /**
     * @return the codigoProvincia
     */
    public String getCodigoProvincia() {
        return codigoProvincia;
    }

    /**
     * @param codigoProvincia the codigoProvincia to set
     */
    public void setCodigoProvincia(String codigoProvincia) {
        this.codigoProvincia = codigoProvincia;
    }

    /**
     * @return the codigoPais
     */
    public String getCodigoPais() {
        return codigoPais;
    }

    /**
     * @param codigoPais the codigoPais to set
     */
    public void setCodigoPais(String codigoPais) {
        this.codigoPais = codigoPais;
    }

    /**
     * @return the codigoPostal
     */
    public String getCodigoPostal() {
        return codigoPostal;
    }

    /**
     * @param codigoPostal the codigoPostal to set
     */
    public void setCodigoPostal(String codigoPostal) {
        this.codigoPostal = codigoPostal;
    }

    /**
     * @return the telfRemitente
     */
    public String getTelfRemitente() {
        return telfRemitente;
    }

    /**
     * @param telfRemitente the telfRemitente to set
     */
    public void setTelfRemitente(String telfRemitente) {
        this.telfRemitente = telfRemitente;
    }

    /**
     * @return the faxRemitente
     */
    public String getFaxRemitente() {
        return faxRemitente;
    }

    /**
     * @param faxRemitente the faxRemitente to set
     */
    public void setFaxRemitente(String faxRemitente) {
        this.faxRemitente = faxRemitente;
    }

    /**
     * @return the emailRemitente
     */
    public String getEmailRemitente() {
        return emailRemitente;
    }

    /**
     * @param emailRemitente the emailRemitente to set
     */
    public void setEmailRemitente(String emailRemitente) {
        this.emailRemitente = emailRemitente;
    }

    /**
     * @return the nombreVia
     */
    public String getNombreVia() {
        return nombreVia;
    }

    /**
     * @param nombreVia the nombreVia to set
     */
    public void setNombreVia(String nombreVia) {
        this.nombreVia = nombreVia;
    }

}