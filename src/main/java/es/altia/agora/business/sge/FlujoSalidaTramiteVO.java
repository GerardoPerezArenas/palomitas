package es.altia.agora.business.sge;

/**
 *
 * @author oscar.rodriguez
 */
public class FlujoSalidaTramiteVO {
    private String codMunicipio;
    private String codProcedimiento;
    private String codigoTramiteOrigen;
    private String numeroCondicionSalida;
    private String numeroSecuencia;
    private String codigoTramiteDestino;

    /**
     * @return the codMunicipio
     */
    public String getCodMunicipio() {
        return codMunicipio;
    }

    /**
     * @param codMunicipio the codMunicipio to set
     */
    public void setCodMunicipio(String codMunicipio) {
        this.codMunicipio = codMunicipio;
    }

    /**
     * @return the codProcedimiento
     */
    public String getCodProcedimiento() {
        return codProcedimiento;
    }

    /**
     * @param codProcedimiento the codProcedimiento to set
     */
    public void setCodProcedimiento(String codProcedimiento) {
        this.codProcedimiento = codProcedimiento;
    }

    /**
     * @return the codigoTramiteOrigen
     */
    public String getCodigoTramiteOrigen() {
        return codigoTramiteOrigen;
    }

    /**
     * @param codigoTramiteOrigen the codigoTramiteOrigen to set
     */
    public void setCodigoTramiteOrigen(String codigoTramiteOrigen) {
        this.codigoTramiteOrigen = codigoTramiteOrigen;
    }

    /**
     * @return the numeroCondicionSalida
     */
    public String getNumeroCondicionSalida() {
        return numeroCondicionSalida;
    }

    /**
     * @param numeroCondicionSalida the numeroCondicionSalida to set
     */
    public void setNumeroCondicionSalida(String numeroCondicionSalida) {
        this.numeroCondicionSalida = numeroCondicionSalida;
    }

    /**
     * @return the numeroSecuencia
     */
    public String getNumeroSecuencia() {
        return numeroSecuencia;
    }

    /**
     * @param numeroSecuencia the numeroSecuencia to set
     */
    public void setNumeroSecuencia(String numeroSecuencia) {
        this.numeroSecuencia = numeroSecuencia;
    }

    /**
     * @return the codigoTramiteDestino
     */
    public String getCodigoTramiteDestino() {
        return codigoTramiteDestino;
    }

    /**
     * @param codigoTramiteDestino the codigoTramiteDestino to set
     */
    public void setCodigoTramiteDestino(String codigoTramiteDestino) {
        this.codigoTramiteDestino = codigoTramiteDestino;
    }
    
}