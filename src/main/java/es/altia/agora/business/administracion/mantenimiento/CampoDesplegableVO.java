package es.altia.agora.business.administracion.mantenimiento;

/**
 *
 * @author Administrador
 */
public class CampoDesplegableVO {
    private String codigoCampo;
    private String codigoValor;
    private String descripcion;

    /**
     * @return the codigoCampo
     */
    public String getCodigoCampo() {
        return codigoCampo;
    }

    /**
     * @param codigoCampo the codigoCampo to set
     */
    public void setCodigoCampo(String codigoCampo) {
        this.codigoCampo = codigoCampo;
    }

    /**
     * @return the codigoValor
     */
    public String getCodigoValor() {
        return codigoValor;
    }

    /**
     * @param codigoValor the codigoValor to set
     */
    public void setCodigoValor(String codigoValor) {
        this.codigoValor = codigoValor;
    }

    /**
     * @return the descripcion
     */
    public String getDescripcion() {
        return descripcion;
    }

    /**
     * @param descripcion the descripcion to set
     */
    public void setDescripcion(String descripcion) {
        this.descripcion = descripcion;
    }
}
